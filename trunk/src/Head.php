<?php

class Head {

    /* =======================================================================
     * Properties
     * ======================================================================= */

    public $content;
    public $title;

    /* =======================================================================
     * Constructors
     * ======================================================================= */

    public function __construct() {
        $this->content = '';
        $this->title = '';
    }

    /* =======================================================================
     * Methods
     * ======================================================================= */

    public function __toString() {
        return $content;
    }
}