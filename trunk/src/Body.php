<?php

class Body {

    /* =======================================================================
     * Properties
     * ======================================================================= */

    public $content;
    public $attributes;

    /* =======================================================================
     * Constructors
     * ======================================================================= */

    public function __construct() {
        $this->content = '';
        $this->attributes = array();
    }

    /* =======================================================================
     * Methods
     * ======================================================================= */

    public function __toString() {
        return $this->content;
    }    
}
